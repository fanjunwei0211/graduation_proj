void LaserSerial2Data(unsigned char ucData)
{
	static unsigned char ucRxBuffer[20];
	static unsigned char ucRxCnt = 0;	
	
	ucRxBuffer[ucRxCnt++]=ucData;	//将收到的数据存入缓冲区中
	if ((ucRxBuffer[0]!=0x04) && (ucRxBuffer[0]!=0x05) && (ucRxBuffer[0]!=0x06)) //数据头不对，则重新开始寻找数据头
	{
		ucRxCnt=0;
		return;
	}
	if (ucRxCnt<8) {return;}//数据不满8个，则返回
	else
	{
		switch(ucRxBuffer[0])//判断数据是哪个激光测距仪返回的
		{
			case 0x04:	laserDis[0] = (ucRxBuffer[4]<<8) | ucRxBuffer[5];break;
			case 0x05:	laserDis[1] = (ucRxBuffer[4]<<8) | ucRxBuffer[5];break;
			case 0x06:	laserDis[2] = (ucRxBuffer[4]<<8) | ucRxBuffer[5];break;
		}
//		MBRTUHandle.usStatus++;
//		MBRTUHandle.timerStart();
		ucRxCnt=0;//清空缓存区
	}
}

