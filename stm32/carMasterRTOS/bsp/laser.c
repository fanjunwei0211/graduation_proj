void laserOpenClose(uint8_t address, int mode){
	uint16_t crc;
	uint8_t ucBuf[8];

	ucBuf[0] = address;
	ucBuf[1] = 0x03;
	ucBuf[2] = 0x00;
	ucBuf[3] = 0x02;
	ucBuf[4] = 0x00;
	ucBuf[5] = mode;

	crc = usMBCRC16( ( uint8_t * )ucBuf, 6);
	ucBuf[6] = ( uint8_t )( crc & 0xFF );
	ucBuf[7] = ( uint8_t )( crc >> 8 );
	HAL_UART_Transmit(&huart2, (uint8_t *)ucBuf, 8, 100);
}


